package com.lanqiao.CRM.entity;

public class Log {
	
	private int log_id;
	private String log_createtime;
	private String log_operation;
	private String log_type;
	public int getLog_id() {
		return log_id;
	}
	public void setLog_id(int log_id) {
		this.log_id = log_id;
	}
	public String getLog_createtime() {
		return log_createtime;
	}
	public void setLog_createtime(String log_createtime) {
		this.log_createtime = log_createtime;
	}
	public String getLog_operation() {
		return log_operation;
	}
	public void setLog_operation(String log_operation) {
		this.log_operation = log_operation;
	}
	public String getLog_type() {
		return log_type;
	}
	public void setLog_type(String log_type) {
		this.log_type = log_type;
	}
	

}
